import streamlit as st
import numpy as np
from PIL import Image
import requests
import os
import tensorflow as tf

# -------------------------------
# CONFIG
# -------------------------------
st.set_page_config(page_title="Fake Face Detection", layout="centered")
st.title("🕵️ Fake Face Detection System")
st.write("Upload an image to check whether it is **REAL** or **FAKE**")

# -------------------------------
# MODEL SETUP
# -------------------------------
TFLITE_MODEL_URL = "https://github.com/shrashttomar-design/Fake-Face-Detection/releases/download/v1.0.0/fake_face_model.tflite"
TFLITE_MODEL_PATH = "fake_face_model.tflite"

def download_tflite_model():
    if not os.path.exists(TFLITE_MODEL_PATH):
        with st.spinner("📥 Downloading TFLite model..."):
            response = requests.get(TFLITE_MODEL_URL, stream=True)
            if response.status_code == 200:
                with open(TFLITE_MODEL_PATH, "wb") as f:
                    for chunk in response.iter_content(chunk_size=8192):
                        if chunk:
                            f.write(chunk)
                st.success("✅ Model downloaded successfully!")
            else:
                st.error("❌ Failed to download model. Check URL.")
                return False
    return True

# Download if missing
if not download_tflite_model():
    st.stop()

# Load TFLite model
interpreter = tf.lite.Interpreter(model_path=TFLITE_MODEL_PATH)
interpreter.allocate_tensors()
input_details = interpreter.get_input_details()
output_details = interpreter.get_output_details()

# -------------------------------
# PREDICTION FUNCTION
# -------------------------------
def predict_tflite(image_array: np.ndarray) -> float:
    img = np.expand_dims(image_array, axis=0).astype(np.float32)
    interpreter.set_tensor(input_details[0]['index'], img)
    interpreter.invoke()
    output = interpreter.get_tensor(output_details[0]['index'])
    return float(output[0][0])

# -------------------------------
# STREAMLIT UI
# -------------------------------
uploaded_file = st.file_uploader("Upload image...", type=["jpg","png","jpeg"])

if uploaded_file:
    # Load and display
    image = Image.open(uploaded_file).convert("RGB").resize((128, 128))
    st.image(image, caption="Uploaded Image", use_container_width=True)
    img_array = np.array(image) / 255.0

    # Prediction
    with st.spinner("🔍 Analyzing image..."):
        confidence = predict_tflite(img_array)

    st.divider()
    st.subheader("Result:")
    if confidence > 0.5:
        st.error(f"❌ FAKE FACE ({confidence*100:.2f}% confidence)")
    else:
        st.success(f"✅ REAL FACE ({(1-confidence)*100:.2f}% confidence)")
